    void loadFormToDomain(){
        if(konfig==null){
            konfig = new Konfigurasi();
        }
        konfig.setDendaPerHari(TextComponentUtils.parseNumberToBigDecimal(txtDenda.getText()));
        konfig.setMaxLamaPinjam(Integer.parseInt(txtMaxLamaPinjam.getText()));
    }
    
    void loadDomainToForm(){
        txtDenda.setText(TextComponentUtils.formatNumber(konfig.getDendaPerHari()));
        txtMaxLamaPinjam.setText(konfig.getMaxLamaPinjam().toString());
    }
    
    void enableForm(Boolean status){
        txtDenda.setEnabled(status);
        txtMaxLamaPinjam.setEnabled(status);
    }
    
    void defaultForm(){
        loadDomainToForm();
        enableForm(false);
        statusButton(Boolean.TRUE, Boolean.FALSE, Boolean.FALSE);
    }
    
    void editForm(){
        enableForm(true);
        statusButton(Boolean.FALSE, Boolean.TRUE, Boolean.TRUE);
    }
    
    void statusButton(Boolean edit, Boolean save, Boolean cancel){
        btnEdit.setEnabled(edit);
        btnSimpan.setEnabled(save);
        btnBatal.setEnabled(cancel);
    }

