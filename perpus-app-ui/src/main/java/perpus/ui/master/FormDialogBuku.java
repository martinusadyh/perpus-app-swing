    public Buku editDialog(Buku buku) {
        this.buku = buku;
        loadDomainToForm();
        setVisible(true);
        return buku;
    }
    
    private void loadDomainToForm() {
        txtKodeBuku.setText(buku.getKodeBuku());
        txtJudulBuku.setText(buku.getJudulBuku());
        txtPengarang.setText(buku.getPengarang());
        txtPenerbit.setText(buku.getPenerbit());
        txtKotaTerbit.setText(buku.getKotaTerbit());
        txtThnTerbit.setDate(buku.getTahunTerbit());
        cmbJnsBuku.getModel().setSelectedItem(buku.getJenisBuku());
        txtJmlBuku.setText(String.valueOf(buku.getJumlahBuku()));
    }
    
    private void loadFormToDomain() {
        if (buku == null) {
            buku = new Buku();
        }
        
        buku.setKodeBuku(txtKodeBuku.getText());
        buku.setJudulBuku(txtJudulBuku.getText());
        buku.setPengarang(txtPengarang.getText());
        buku.setPenerbit(txtPenerbit.getText());
        buku.setKotaTerbit(txtKotaTerbit.getText());
        buku.setTahunTerbit(txtThnTerbit.getDate());
        buku.setJenisBuku((JenisBuku)cmbJnsBuku.getSelectedItem());
        buku.setJumlahBuku(Integer.valueOf(txtJmlBuku.getText()));
    }
    
    private Boolean validateForm() {
        if (txtKodeBuku.getText().length() > 0 && 
            txtJudulBuku.getText().length() > 0 &&
            txtThnTerbit.getDate() != null) {
            return true;
        } else {
            return false;
        }
    }

